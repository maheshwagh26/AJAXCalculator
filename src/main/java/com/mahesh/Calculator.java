package com.mahesh;

public class Calculator {
	
		public int addFunction(int num1,int num2) {
			return num1 + num2;
			
		}
		public int substractFunction(int num1,int num2) {
			return num1 - num2;
			
		}
		public int multiplyFunction(int num1,int num2) {
			return num1 * num2;
			
		}
		public int divideFunction(int num1,int num2) {
			return num1 / num2;
			
		}
}
